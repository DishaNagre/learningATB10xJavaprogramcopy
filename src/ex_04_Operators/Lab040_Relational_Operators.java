package ex_04_Operators;

public class Lab040_Relational_Operators {
    public static void main(String[] args) {
        // < less than
        // <= less than or equal to
        // > greater than
        //>= Greater or equal
        // == Equal to
        // != not equal
        // all of them will result boolean output.

        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);
        boolean d = a < b;
        System.out.println(d);
        boolean e = a == b;
        System.out.println(e);
        boolean f = a != b;
        System.out.println(f);

        // interview q

        int p =10;
        int q =20;
        System.out.println(p+q);

        String name1 = "Disha";
        String name2 = "Nagre";
        System.out.println(name1+name2); // Concatenation

        // Plus + will behave differently with the data types


    }
}